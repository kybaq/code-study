#1.문제 이해
# 1) 정수 K가 주어짐:
    # K는 입력되는 숫자의 개수
# 2) 각 줄마다 입력된 정수 처리:
    # 숫자가 0이면 가장 최근에 기록한 숫자를 제거
    # 숫자가 0이 아니면 해당 숫자를 기록
# 3) 최종적으로 기록된 숫자들의 합을 출력


#2.예제 이해
# 예제1
# 	입력: 4,3,0,4,0
# 	처리 과정:
# 		1) 3을 기록 => [3]
#       2) 0이 입력 => 최근 기록 제거 → []
#       3) 4를 기록 => [4]
#       4) 0이 입력 => 최근 기록 제거 → []
#   출력: 합계 = 0

